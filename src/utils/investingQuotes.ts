export function getDailyQuote(date: string): InvestingQuote {
  const seed = date.replace(/-/g, '').split('').reduce((acc, c) => acc + c.charCodeAt(0), 0)
  return INVESTING_QUOTES[seed % INVESTING_QUOTES.length]
}
